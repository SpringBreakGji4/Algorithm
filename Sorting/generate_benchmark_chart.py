from __future__ import annotations

import argparse
import csv
import subprocess
from collections import defaultdict
from io import StringIO
from pathlib import Path

ROOT = Path(__file__).resolve().parent
RESULTS_DIR = ROOT / "results"
FAST_KEYS = {"shell", "merge", "heap", "quick", "radix", "bucket", "baseline"}
SLOW_KEYS = {"bubble", "selection", "insertion"}
BASELINE_KEYS = {"qsort", "arrays", "python"}

COMMANDS = {
    "C": ["../bin/sorting_demo", "benchmark-tsv", "1"],
    "Java": ["java", "-cp", "bin", "SortingDemo", "benchmark-tsv", "1"],
    "Python": ["python3", "sorting_demo.py", "benchmark-tsv", "1"],
}

WORKDIRS = {
    "C": ROOT / "C",
    "Java": ROOT / "Java",
    "Python": ROOT / "Python",
}

BUILD_STEPS = [
    (["make", "demo"], ROOT),
    (["javac", "-d", "bin", "src/SortingDemo.java"], ROOT / "Java"),
]


def run_command(command: list[str], cwd: Path) -> str:
    completed = subprocess.run(command, cwd=cwd, check=True, capture_output=True, text=True)
    return completed.stdout


def parse_tsv_output(output: str) -> list[dict[str, str]]:
    lines = [line for line in output.splitlines() if line.strip()]
    return list(csv.DictReader(StringIO("\n".join(lines)), delimiter="\t"))


def normalize_key(row: dict[str, str]) -> str:
    key = row["key"]
    if key in BASELINE_KEYS:
        return "baseline"
    return key


def normalize_name(row: dict[str, str]) -> str:
    key = normalize_key(row)
    if key == "baseline":
        return "Standard library sort"
    return row["name"]


def include_key(key: str, mode: str) -> bool:
    if mode == "full":
        return True
    if mode == "slow-only":
        return key in SLOW_KEYS
    return key in FAST_KEYS


def load_rows(repeats: int, mode: str) -> list[dict[str, str]]:
    for command, cwd in BUILD_STEPS:
        subprocess.run(command, cwd=cwd, check=True)

    rows: list[dict[str, str]] = []
    for language, command in COMMANDS.items():
        command[-1] = str(repeats)
        output = run_command(command, WORKDIRS[language])
        for row in parse_tsv_output(output):
            row["normalized_key"] = normalize_key(row)
            row["normalized_name"] = normalize_name(row)
            if include_key(row["normalized_key"], mode):
                rows.append(row)
    return rows


def write_csv(rows: list[dict[str, str]], path: Path) -> None:
    fieldnames = [
        "language",
        "normalized_key",
        "normalized_name",
        "complexity",
        "average_us",
        "total_us",
        "passed",
        "repeats",
    ]
    with path.open("w", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow({key: row.get(key, "") for key in fieldnames})


def mermaid_chart(title: str, labels: list[str], values: list[int]) -> str:
    quoted_labels = ", ".join(f'"{label}"' for label in labels)
    value_list = ", ".join(str(value) for value in values)
    max_value = max(values) if values else 0
    upper = max(1, int(max_value * 1.15))
    return (
        f"### {title}\n\n"
        "```mermaid\n"
        "xychart-beta\n"
        f'    title "{title}"\n'
        f"    x-axis [{quoted_labels}]\n"
        f'    y-axis "Average usec" 0 --> {upper}\n'
        f"    bar [{value_list}]\n"
        "```\n"
    )


def write_markdown(rows: list[dict[str, str]], path: Path, repeats: int, mode: str) -> None:
    grouped: dict[str, list[dict[str, str]]] = defaultdict(list)
    for row in rows:
        grouped[row["language"]].append(row)

    lines: list[str] = []
    lines.append("# Sorting benchmark chart report")
    lines.append("")
    lines.append(f"- Mode: `{mode}`")
    lines.append(f"- Repeats per testcase: `{repeats}`")
    lines.append("")
    lines.append("This file is generated by `generate_benchmark_chart.py`.\n")

    for language in ("C", "Java", "Python"):
        entries = sorted(grouped.get(language, []), key=lambda row: int(row["average_us"]))
        if not entries:
            continue
        labels = [row["normalized_name"].replace(" sort", "") for row in entries]
        values = [int(row["average_us"]) for row in entries]
        lines.append(mermaid_chart(f"{language} benchmark results", labels, values))
        lines.append("| Rank | Algorithm | Avg (usec) | Complexity |")
        lines.append("|---|---|---:|---|")
        for index, row in enumerate(entries, start=1):
            lines.append(
                f"| {index} | {row['normalized_name']} | {int(row['average_us'])} | {row['complexity']} |"
            )
        lines.append("")

    path.write_text("\n".join(lines))


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate CSV + Mermaid chart report for sorting benchmarks.")
    parser.add_argument("repeats", nargs="?", default=3, type=int, help="Runs per testcase")
    parser.add_argument("--mode", choices=["fast", "full", "slow-only"], default="fast")
    args = parser.parse_args()

    if args.repeats <= 0:
        raise SystemExit("repeats must be positive")

    RESULTS_DIR.mkdir(exist_ok=True)
    rows = load_rows(args.repeats, args.mode)
    write_csv(rows, RESULTS_DIR / "benchmark_results.csv")
    write_markdown(rows, RESULTS_DIR / "benchmark_chart.md", args.repeats, args.mode)

    print(f"Wrote {RESULTS_DIR / 'benchmark_results.csv'}")
    print(f"Wrote {RESULTS_DIR / 'benchmark_chart.md'}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
